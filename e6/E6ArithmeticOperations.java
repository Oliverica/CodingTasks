package org.example.e6;

public class E6ArithmeticOperations {
    public static void main(String[] args) {
        // Step 1: Declare the float variable and set it to 8.2
        float number = 8.2f;


        // Step 2: Multiply the value by itself and store the result
        float result = number * number;


        // Step 3: Print the result
        //The multiplication of 8.2 with itself is equal to 67.24

        System.out.println(" The multiplication of " + number + " with itself is equal to " + result);

    }
}
